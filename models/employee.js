let mongoose = require('mongoose');

//Employee Schema
let employeeSchema = mongoose.Schema({
    employeeId: {
        type: String,
        required: true
    },
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    salary: {
        type: String,
        required: true
    },
    birthDate: {
        type: String,
        required: true
    },
    HireDate: {
        type: String,
        required: true
    }
});

let Employee = module.exports = mongoose.model("Employee", employeeSchema);


